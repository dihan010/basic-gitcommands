// comparison operators > >= < <= === == != !==

var ageLimon=30;
var ageDihan=24;
console.log(ageLimon>ageDihan);
console.log(ageLimon>=ageDihan);
console.log(ageLimon<ageDihan);
console.log(ageLimon<=ageDihan);
console.log(ageLimon===ageDihan); //strict equality
console.log(ageLimon==ageDihan); //loose equality
console.log(ageLimon!=ageDihan); //loose inequality
console.log(ageLimon!==ageDihan); //strict inequality